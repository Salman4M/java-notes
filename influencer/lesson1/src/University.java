public class University {
    public String name;
    public String Address;

    public String getName() {
        return name;
    }

    public University setName(String name) {
        this.name = name;
        return this;
    }

    public String getAddress() {
        return Address;
    }

    public University setAddress(String address) {
        Address = address;
        return this;
    }
}
